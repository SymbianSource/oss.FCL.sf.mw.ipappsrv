// ---------------------------------------------------------------------------
// CTest::CTest
// C++ default constructor can NOT contain any code, that
// might leave.
// ---------------------------------------------------------------------------
//
CTest::CTest()
    {
    }
    
// ---------------------------------------------------------------------------
// CTest::ConstructL
// Symbian 2nd phase constructor can leave.
// ---------------------------------------------------------------------------
//
void CTest::ConstructL( CConsoleBase& aConsole, TMccNetSettings aNetsettings )
    {
    iConsole = &aConsole;
    iNetsettings = aNetsettings;
    }
    
// ---------------------------------------------------------------------------
// CTest::NewL
// Static constructor.
// ---------------------------------------------------------------------------
//   
CTest* CTest::NewL( CConsoleBase& aConsole, TMccNetSettings aNetsettings )
    {
    CTest* self = new ( ELeave ) CTest();
    CleanupStack::PushL( self );

    self->ConstructL( aConsole, aNetsettings );

    CleanupStack::Pop( self );
    return self;
    }

// ---------------------------------------------------------------------------
// CTest::~CTest
// Destructor.
// ---------------------------------------------------------------------------
//
CTest::~CTest()
    {
    REComSession::FinalClose();
    }

